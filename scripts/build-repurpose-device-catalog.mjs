const raw = fs.readFileSync(csvPath, "utf8").trim().split(/\r?\n/);
const header = raw[0];
if (!header?.toLowerCase().includes("model_exif")) {
  console.error("Unexpected CSV header:", header);
  process.exit(1);
}

const usedIds = new Set();
const entries = [];

for (let i = 1; i < raw.length; i++) {
  const line = raw[i].trim();
  if (!line) continue;
  const p = parseCsvLine(line);
  if (p.length < 6) continue;
  const [make, modelExif, marketingName, category, manufacturer, releaseYearRaw, ...rest] = p;
  const notes = rest.join(",").trim();
  const releaseYear = parseInt(releaseYearRaw, 10) || 2020;

  const mapped = IPHONE_MARKETING_TO_ID.get(marketingName?.trim());
  if (mapped) continue;

  const base = slug(`${manufacturer || make}-${modelExif}-${marketingName}`) || `row-${i}`;
  let id = base;
  let n = 0;
  while (usedIds.has(id)) {
    n += 1;
    id = `${base}-${n}`;
  }
  usedIds.add(id);

  entries.push({
    id,
    make: make?.trim(),
    modelExif: modelExif?.trim(),
    marketingName: marketingName?.trim(),
    category: category?.trim(),
    manufacturer: manufacturer?.trim(),
    releaseYear,
    notes,
  });
}

const json = JSON.stringify(entries, null, 0);
const file = `/** Auto-generated by scripts/build-repurpose-device-catalog.mjs — do not edit by hand */\nexport const EXTRA_DEVICE_CATALOG = ${json};\n`;
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, file, "utf8");
console.log("Wrote", entries.length, "entries to", path.relative(root, outPath));
